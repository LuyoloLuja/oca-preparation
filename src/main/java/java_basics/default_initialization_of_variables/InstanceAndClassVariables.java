package java_basics.default_initialization_of_variables;

public class InstanceAndClassVariables {
//    instance variables and class variables are declared within a class outside methods
//    also known as fields
//    class variables are declared using the 'static' keyword
//    they are given default values when initialized e.g boolean is given false, int 0, String null...

    String name; // null
    int number; // 0
    boolean x; // false

//                    Variable Type                     |   Default Values
//                    _________________________________________________________________
//                      boolean                         |   false
//                    -----------------------------------------------------------------
//                      byte, short, int, long          |   0
//                    -----------------------------------------------------------------
//                      float, double                   |   0.0
//                    -----------------------------------------------------------------
//                      char                            |   (NULL)
//                    -----------------------------------------------------------------
//                      All object references           |   null
}
